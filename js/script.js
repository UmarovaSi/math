
    //  FLOOR
function floor(number) {
    let total = Math.floor(number);
    return total;
}

let total = floor(-5,3);
console.log(total);


    //   CEIL
function ceil(num) {
    let result = Math.ceil(num);
    return result;
}
        
let result = ceil(5,3);
console.log(result);


